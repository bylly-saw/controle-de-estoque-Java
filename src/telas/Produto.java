    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (flag == 0) {
            if (JOptionPane.showConfirmDialog(rootPane, "Deseja realmente alterar?", "Confirmar Alteração", JOptionPane.YES_NO_OPTION) == 0) {
                try {
                    //produto.setCodp(Integer.parseInt(jTextFieldCod.getText()));
                    produto.setCod_barras(jTextFieldCodBar.getText());
                    produto.setNome(jTextFieldProduto.getText());
                    produto.setQuantidade(Integer.parseInt(jTextFieldQtde.getText()));
                    produto.setEstoque_minimo(Integer.parseInt(jTextFieldEstMin.getText()));
                    String precoComp = jTextFieldPrecoComp.getText().replace(",", ".");
                    String precoVenda = jTextFieldPrecoVenda.getText().replace(",", ".");
                    produto.setPreco_compra(Float.parseFloat(precoComp));
                    produto.setPreco_venda(Float.parseFloat(precoVenda));
                    produto.setData(jFormattedTextFieldData.getText());
                    produto.setFornecedor((String) jComboBoxForn.getSelectedItem());
                    produto.setCategoria((String) jComboBoxCat.getSelectedItem());

                    if (control.alterar(produto)) {
                        this.estadoInicial();
                        this.preencherTabela("select distinct * from view_produtos_recentes_detalhes order by nome");
                    }
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(this, "Formato de número inválido!\n" + ex.getMessage());
                }
            }
        } else {
            try {
                produto.setCod_barras(jTextFieldCodBar.getText());
                produto.setNome(jTextFieldProduto.getText());
                produto.setQuantidade(Integer.parseInt(jTextFieldQtde.getText()));
                produto.setEstoque_minimo(Integer.parseInt(jTextFieldEstMin.getText()));
                String precoComp = jTextFieldPrecoComp.getText().replace(",", ".");
                String precoVenda = jTextFieldPrecoVenda.getText().replace(",", ".");
                produto.setPreco_compra(Float.parseFloat(precoComp));
                produto.setPreco_venda(Float.parseFloat(precoVenda));
                produto.setData(jFormattedTextFieldData.getText());
                produto.setFornecedor((String) jComboBoxForn.getSelectedItem());
                produto.setCategoria((String) jComboBoxCat.getSelectedItem());

                if (control.inserir(produto)) {
                    this.estadoInicial();
                    this.preencherTabela("select distinct * from view_produtos_recentes_detalhes order by nome");
                    flag = 0;
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Formato de número inválido!\n" + ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed


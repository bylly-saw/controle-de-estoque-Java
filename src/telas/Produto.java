    private void preencherTabela(String sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Nome produto", "Preço de venda R$", "Preço de compra R$", "Quantidade"};
        int i = 0;
        conex.conexao();
        try {
            conex.executaSQL(sql);
            conex.rs.first();
            do {
                i++;
                dados.add(new Object[]{conex.rs.getString("nome"), conex.rs.getBigDecimal("preco_venda"),
                    conex.rs.getBigDecimal("preco_compra"), conex.rs.getInt("quantidade")});
            } while (conex.rs.next());
        } catch (SQLException ex) {
            //JOptionPane.showMessageDialog(this, "Erro ao preencher Arraylist!\n"+ex.getMessage());
        }

        ModeloTabela modelotabela = new ModeloTabela(dados, colunas);
        tabela.setModel(modelotabela);
        if (i > 5) {
            tabela.getColumnModel().getColumn(0).setPreferredWidth(132);
            tabela.getColumnModel().getColumn(1).setPreferredWidth(132);
            tabela.getColumnModel().getColumn(2).setPreferredWidth(132);
            tabela.getColumnModel().getColumn(3).setPreferredWidth(132);
        } else {
            tabela.getColumnModel().getColumn(0).setPreferredWidth(136);
            tabela.getColumnModel().getColumn(1).setPreferredWidth(136);
            tabela.getColumnModel().getColumn(2).setPreferredWidth(136);
            tabela.getColumnModel().getColumn(3).setPreferredWidth(135);
        }
        tabela.getColumnModel().getColumn(0).setResizable(false);
        tabela.getColumnModel().getColumn(1).setResizable(false);
        tabela.getColumnModel().getColumn(2).setResizable(false);
        tabela.getColumnModel().getColumn(3).setResizable(false);
        tabela.getTableHeader().setReorderingAllowed(false);
        tabela.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tabela.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        conex.desconecta();
    }


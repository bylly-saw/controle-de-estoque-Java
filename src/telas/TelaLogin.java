    private void jButtonAcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAcessarActionPerformed
        try {
            conex.executaSQL("select * from usuario where login='" + jTextFieldLogin.getText() + "'");
            conex.rs.first();
            if (conex.rs.getString("senha").equals(jPasswordFieldSenha.getText())) {
                new TelaInicial(jTextFieldLogin.getText()).setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "O usuário ou a senha não correspondem!");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "O usuário ou a senha não correspondem!");
        }
    }//GEN-LAST:event_jButtonAcessarActionPerformed


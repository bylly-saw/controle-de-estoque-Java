    private void tabelaMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseEntered
        if (jTextFieldPesqCid.getText().equals("")) {
            this.preencherTabela("select * from cidade order by nome, uf  limit 100");
        } else {
            this.preencherTabela("select * from cidade where nome ~* '" + jTextFieldPesqCid.getText() + "' order by nome, uf");
        }
    }//GEN-LAST:event_tabelaMouseEntered

    private void tabelaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tabelaKeyPressed
        if (evt.getKeyCode() == VK_ENTER) {
            evt.consume();
            String nome = "" + tabela.getValueAt(tabela.getSelectedRow(), 0);
            String uf = "" + tabela.getValueAt(tabela.getSelectedRow(), 1);
            new TelaAlterarCidade(nome, uf).setVisible(true);
        } else if (evt.getKeyCode() == VK_ESCAPE) {
            jTextFieldPesqCid.requestFocus();
        }
    }//GEN-LAST:event_tabelaKeyPressed


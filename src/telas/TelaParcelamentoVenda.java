    private void jButtonEfetuarPagActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEfetuarPagActionPerformed
        if (flag == 1) {
            int codVenda = (int) tabela.getValueAt(tabela.getSelectedRow(), 0);
            int numParc = (int) tabelaParcelasVenda.getValueAt(tabelaParcelasVenda.getSelectedRow(), 0);
            //pra não dar erro de clique mal feito
            if (codVenda == codv) {
                if (!"PAGA".equals(String.valueOf(tabelaParcelasVenda.getValueAt(tabelaParcelasVenda.getSelectedRow(), 3)))) {
                    this.alterarSituacao(codVenda, numParc);
                    this.preencherTabelaParcelas("select * from view_venda_parcelada where codv =" + codVenda + " order by num_parcela");
                } else {
                    JOptionPane.showMessageDialog(null, "Esta parcela já está paga!");
                }
            }
        }
    }//GEN-LAST:event_jButtonEfetuarPagActionPerformed


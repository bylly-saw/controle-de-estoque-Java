    public void alterarSituacao(int codVenda, int numParc) {
        conex.conexao();
        try {
            PreparedStatement pst = conex.conn.prepareStatement("update parcela_venda set situacao='PAGA' where codv=? and num_parcela=?");
            pst.setInt(1, codVenda);
            pst.setInt(2, numParc);
            pst.execute();

            JOptionPane.showMessageDialog(null, "Parcela paga com sucesso!");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao pagar o produto!\n" + ex.getMessage().replaceAll("ERROR:", ""));
        }
        conex.desconecta();
    }


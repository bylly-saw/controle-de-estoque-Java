    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        try {
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
            format.setLenient(false);
            Date dataIni = new Date(format.parse(jFormattedTextFieldDataIni.getText()).getTime());
            Date dataFim = new Date(format.parse(jFormattedTextFieldDataFim.getText()).getTime());

            this.preencherTabelaParcelas("select * from view_venda_parcelada where codpr=0");

            if (jRadioButtonDataHoraVenda.isSelected()) {
                this.preencherTabela("select * from view_venda_detalhes "
                        + "where tipo='À prazo' and data between '" + dataIni + "' and '" + dataFim + "' "
                        + "and hora between '" + jFormattedTextFieldHoraIni.getText() + "' and '" + jFormattedTextFieldHoraFim.getText() + "' "
                        + " order by codv");
            } else if (jRadioButtonCodVenda.isSelected()) {
                if (!jTextFieldPesquisa.getText().equals("")) {
                    this.preencherTabela("select * from view_venda_detalhes "
                            + "where tipo='À prazo' and codv=" + jTextFieldPesquisa.getText()
                            + " order by codv");
                } else {
                    this.preencherTabela("select * from view_venda_detalhes "
                            + "where tipo='À prazo' and codv=0"
                            + " order by codv");
                }
            } else if (jRadioButtonCliente.isSelected()) {
                if (jTextFieldPesquisa.getText().equals("")) {
                    this.preencherTabela("select * from view_venda_detalhes where tipo='À prazo' order by codv limit 100");
                } else {
                    this.preencherTabela("select * from view_venda_detalhes "
                            + "where tipo='À prazo' and nome_cliente ~* '" + jTextFieldPesquisa.getText() + "' "
                            + " order by codv");
                }
            }
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Data ou hora inválida!");
            //Logger.getLogger(TelaPesquisaVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed


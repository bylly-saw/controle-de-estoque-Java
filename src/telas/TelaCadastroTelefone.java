    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        //categoria.setCodcat(Integer.parseInt(jTextFieldCod.getText()));
        if (flag == 0) {
            if (JOptionPane.showConfirmDialog(rootPane, "Deseja realmente alterar?", "Confirmar AlteraÃ§Ã£o", JOptionPane.YES_NO_OPTION) == 0) {
                telefone.setNumero_tel(jFormattedTextNum.getText());
                telefone.setTipo(tipo);

                if (control.alterar(telefone)) {
                    this.estadoInicial();
                    this.preencherTabela("select * from telefone where tipo='" + tipo + "' order by numero_tel");
                }
            }
        } else {
            telefone.setNumero_tel(jFormattedTextNum.getText());
            telefone.setTipo(tipo);

            if (control.salvar(telefone)) {
                this.estadoInicial();
                this.preencherTabela("select * from telefone where tipo='" + tipo + "' order by numero_tel");
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed


    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        // nÃ£o pode ser ativada quando o botÃ£o novo for acionado!!!!! 
        if (flag == 0) {
            String numero_tel = "" + tabela.getValueAt(tabela.getSelectedRow(), 0);
            conex.conexao();
            conex.executaSQL("select * from telefone where numero_tel='" + numero_tel + "'");
            try {
                conex.rs.first();
                telefone.setCodt(conex.rs.getInt("codt"));
                jFormattedTextNum.setText(conex.rs.getString("numero_tel"));
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Erro ao selecionar os dados!\n" + ex.getMessage());
            }
            conex.desconecta();
            jFormattedTextNum.setEnabled(true);
            jButtonNovo.setEnabled(false);
            jButtonSalvar.setEnabled(true);
            jButtonExcluir.setEnabled(true);
            jButtonCancelar.setEnabled(true);
        }
    }//GEN-LAST:event_tabelaMouseClicked


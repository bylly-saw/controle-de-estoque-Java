    private void preencherComboUsu() {
        conex.conexao();
        conex.executaSQL("select * from usuario order by nome");
        Object obj = jComboBoxUsu.getSelectedItem();
        jComboBoxUsu.removeAllItems();
        jComboBoxUsu.addItem("");
        try {
            conex.rs.first();
            do {
                jComboBoxUsu.addItem(conex.rs.getString("nome"));
            } while (conex.rs.next());
        } catch (SQLException ex) {
            //JOptionPane.showMessageDialog(this, "Erro ao preencher combobox!\n"+ex.getMessage());
        }
        jComboBoxUsu.setSelectedItem(obj);
        conex.desconecta();
    }


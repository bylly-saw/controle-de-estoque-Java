    private void jComboBoxTipoMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jComboBoxTipoMouseEntered
        if (jRadioButtonCli.isSelected()) {
            Object obj = jComboBoxTipo.getSelectedItem();
            this.preencherComboBox("Cliente");
            jComboBoxTipo.setSelectedItem(obj);
        } else if (jRadioButtonForn.isSelected()) {
            Object obj = jComboBoxTipo.getSelectedItem();
            this.preencherComboBox("Fornecedor");
            jComboBoxTipo.setSelectedItem(obj);
        } else if (jRadioButtonProd.isSelected()) {
            Object obj = jComboBoxTipo.getSelectedItem();
            this.preencherComboBox("Produto");
            jComboBoxTipo.setSelectedItem(obj);
        } else if (jRadioButtonVenda.isSelected()) {
            Object obj = jComboBoxTipo.getSelectedItem();
            this.preencherComboBox("Venda");
            jComboBoxTipo.setSelectedItem(obj);
        }
    }//GEN-LAST:event_jComboBoxTipoMouseEntered


    private void preencherComboBox(String opc) {
        switch (opc) {
            case "Cliente":
                jComboBoxTipo.removeAllItems();
                jComboBoxTipo.addItem("");
                jComboBoxTipo.addItem("Clientes cadastrados");
                jComboBoxTipo.setSelectedItem("Clientes cadastrados");
                break;
            case "Fornecedor":
                jComboBoxTipo.removeAllItems();
                jComboBoxTipo.addItem("");
                jComboBoxTipo.addItem("Fornecedores cadastrados");
                jComboBoxTipo.setSelectedItem("Fornecedores cadastrados");
                break;
            case "Produto":
                jComboBoxTipo.removeAllItems();
                jComboBoxTipo.addItem("");
                jComboBoxTipo.addItem("Produtos Cadastrados");
                jComboBoxTipo.addItem("Estoque insuficiente");
                jComboBoxTipo.addItem("Produtos n茫o vendidos");
                jComboBoxTipo.addItem("Produtos mais vendidos");
                jComboBoxTipo.setSelectedItem("Produtos Cadastrados");
                break;
            case "Venda":
                jComboBoxTipo.removeAllItems();
                jComboBoxTipo.addItem("");
                jComboBoxTipo.addItem("Vendas por per铆odo");
                jComboBoxTipo.setSelectedItem("Vendas por per铆odo");
                break;
            default:
                break;
        }
    }


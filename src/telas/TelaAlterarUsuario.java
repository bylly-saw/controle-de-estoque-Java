    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (jPasswordFieldSenha.getText().equals(jPasswordFieldConfSenha.getText())) {
            usuario.setLogin(jTextFieldLogin.getText());
            usuario.setNome(jTextFieldNome.getText());
            //login.setPermissao((String)jComboBoxPerm.getSelectedItem());
            usuario.setSenha(jPasswordFieldSenha.getText());

            privilegios.setLogins(jCheckBoxCadLogins.isSelected());
            privilegios.setCategorias(jCheckBoxCadCat.isSelected());
            privilegios.setClientes(jCheckBoxCadClientes.isSelected());
            privilegios.setFornecedores(jCheckBoxCadForn.isSelected());
            privilegios.setProdutos(jCheckBoxCadProd.isSelected());
            privilegios.setPesq_vendas(jCheckBoxPesqVendas.isSelected());
            privilegios.setEfetuar_vendas(jCheckBoxVendas.isSelected());
            privilegios.setRelatorios(jCheckBoxRelatorios.isSelected());

            if (control.alterar(usuario, privilegios)) {
                this.dispose();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar o usuário!\nAs senhas não correspondem.");
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        if (JOptionPane.showConfirmDialog(rootPane, "Deseja realmente excluir?", "Confirmar Exclusão", JOptionPane.YES_NO_OPTION) == 0) {
            //usuario.setCodu(Integer.parseInt(jTextFieldCod.getText()));
            usuario.setLogin(jTextFieldLogin.getText());
            usuario.setNome(jTextFieldNome.getText());
            usuario.setCodpv(privilegios.getCodpv());
            //login.setPermissao((String)jComboBoxPerm.getSelectedItem());
            control.excluir(usuario);
            this.dispose();
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed


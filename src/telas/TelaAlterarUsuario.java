    public TelaAlterarUsuario(String nome) {
        initComponents();
        getRootPane().setDefaultButton(jButtonSalvar);

        conex.conexao();
        conex.executaSQL("select * from view_usuario_detalhes where nome='" + nome + "'");
        try {
            conex.rs.first();
            usuario.setCodu(conex.rs.getInt("codu"));
            jTextFieldLogin.setText(conex.rs.getString("login"));
            jTextFieldNome.setText(conex.rs.getString("nome"));
            privilegios.setCodpv(conex.rs.getInt("codpv"));
            jCheckBoxCadLogins.setSelected(conex.rs.getBoolean("logins"));
            jCheckBoxCadCat.setSelected(conex.rs.getBoolean("categorias"));
            jCheckBoxCadClientes.setSelected(conex.rs.getBoolean("clientes"));
            jCheckBoxCadForn.setSelected(conex.rs.getBoolean("fornecedores"));
            jCheckBoxCadProd.setSelected(conex.rs.getBoolean("produtos"));
            jCheckBoxPesqVendas.setSelected(conex.rs.getBoolean("pesq_vendas"));
            jCheckBoxVendas.setSelected(conex.rs.getBoolean("efetuar_vendas"));
            jCheckBoxRelatorios.setSelected(conex.rs.getBoolean("relatorios"));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao selecionar os dados!\n" + ex.getMessage().replaceAll("ERROR:", ""));
        }
        conex.desconecta();
    }


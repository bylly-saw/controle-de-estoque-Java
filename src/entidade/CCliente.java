package entidade;

/**
 *
 * @author Henike
 */
public class CCliente {

    private int codc;
    private String nome;
    private String rg;
    private String orgao_expedidor;
    private String cpf;
    private String email;
    private String telefone;
    private String celular;

    /**
     * @return the codc
     */
    public int getCodc() {
        return codc;
    }

    /**
     * @param codc the codc to set
     */
    public void setCodc(int codc) {
        this.codc = codc;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the rg
     */
    public String getRg() {
        return rg;
    }

    /**
     * @param rg the rg to set
     */
    public void setRg(String rg) {
        this.rg = rg;
    }

    /**
     * @return the orgao_expedidor
     */
    public String getOrgao_expedidor() {
        return orgao_expedidor;
    }

    /**
     * @param orgao_expedidor the orgao_expedidor to set
     */
    public void setOrgao_expedidor(String orgao_expedidor) {
        this.orgao_expedidor = orgao_expedidor;
    }

    /**
     * @return the cpf
     */
    public String getCpf() {
        return cpf;
    }

    /**
     * @param cpf the cpf to set
     */
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the telefone
     */
    public String getTelefone() {
        return telefone;
    }

    /**
     * @param telefone the telefone to set
     */
    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    /**
     * @return the celular
     */
    public String getCelular() {
        return celular;
    }

    /**
     * @param celular the celular to set
     */
    public void setCelular(String celular) {
        this.celular = celular;
    }

}
